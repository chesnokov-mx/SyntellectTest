// Инициализируем переменные
let peopleInRoom = 1; // начальное количество людей в комнате
let handshakeCount = 0; // счетчик рукопожатий

// Цикл для каждого нового человека, входящего в комнату
for (let i = 2; i <= 10; i++) {
  // Количество рукопожатий каждого нового человека с остальными в комнате равно числу людей в комнате перед его приходом
  handshakeCount += peopleInRoom;
  // Увеличиваем количество людей в комнате на 1
  peopleInRoom++;
}

console.log("Количество рукопожатий для 10 человек: " + handshakeCount);
