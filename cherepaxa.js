let distanceToTop = 100; // расстояние до вершины холма
let climbedDistance = 0; // пройденное расстояние
let days = 0; // количество дней

// Цикл будет продолжаться, пока расстояние до вершины больше 0
while (distanceToTop > 0) {
  // Увеличиваем количество дней
  days++;
  // Черепашка забирается вверх на 50 м
  climbedDistance += 50;
  // Проверяем, достигла ли черепашка вершины холма
  if (climbedDistance >= distanceToTop) {
    break; // Если достигла, выходим из цикла
  }
  // Черепашка спускается на 30 м
  climbedDistance -= 30;
}

console.log("Черепашка залезет на столб на " + days + " сутки.");
